package com.containtment;

public class City {

	private int id;
	private String name;
	private String famous;
	
	public void setId(int id)
	{
		this.id=id;
	}
	public int getId()
	{
		return id;
	}
	public void setName(String name)
	{
		this.name=name;	
	}
	public String getName()
	{
		return name;
	}
	public void setFamous(String famous)
	{
		this.famous=famous;
	}
	public String getFamous()
	{
		return famous;
	}
	
	
	
	
}
